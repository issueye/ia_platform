import fs from "node:fs";
import path from "node:path";
import url from "node:url";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const extensionRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(extensionRoot, "..", "..");
const sourceDoc = path.join(repoRoot, "docs", "2026-04-07", "NATIVE_MODULES.md");
const outFile = path.join(extensionRoot, "src", "generated", "nativeSymbols.ts");

const markdown = fs.readFileSync(sourceDoc, "utf8");
const lines = markdown.split(/\r?\n/);

const moduleSet = new Set();
const symbolMap = new Map();

let currentModule = "";
let currentScope = "";
let inFunctions = false;
let inSupportedModules = false;

function addSymbol(label, kind, detail, doc) {
  if (!label) {
    return;
  }
  if (!symbolMap.has(label)) {
    symbolMap.set(label, { label, kind, detail, doc });
  }
}

for (const raw of lines) {
  const line = raw.trim();

  const h2 = line.match(/^##\s+(.+)$/);
  if (h2) {
    currentModule = h2[1].trim();
    currentScope = currentModule;
    inFunctions = false;
    inSupportedModules = false;
    if (!currentModule.includes(" ")) {
      moduleSet.add(currentModule);
      addSymbol(currentModule, "Module", "module", `${currentModule} module.`);
    }
    continue;
  }

  const h3 = line.match(/^###\s+(.+)$/);
  if (h3) {
    currentScope = h3[1].trim();
    inFunctions = false;
    inSupportedModules = false;
    if (currentScope.includes(".")) {
      addSymbol(currentScope, "Property", "object", `${currentScope} object.`);
    }
    continue;
  }

  if (line === "支持模块:") {
    inSupportedModules = true;
    inFunctions = false;
    continue;
  }

  if (line === "函数:") {
    inFunctions = true;
    inSupportedModules = false;
    continue;
  }

  if (line === "") {
    // Keep current collection mode across blank lines.
    continue;
  }

  if (inSupportedModules) {
    const m = line.match(/^-\s+`([^`]+)`/);
    if (m) {
      const mod = m[1].trim();
      moduleSet.add(mod);
      addSymbol(mod, "Module", "module", `${mod} module.`);
    }
    continue;
  }

  if (inFunctions) {
    const m = line.match(/^-\s+`([^`]+)`/);
    if (!m) {
      continue;
    }
    const sig = m[1].trim();
    const nameMatch = sig.match(/^([A-Za-z_][A-Za-z0-9_]*)\s*(\(|$)/);
    if (!nameMatch) {
      continue;
    }
    const fnName = nameMatch[1];
    const scope = currentScope || currentModule;
    if (!scope || scope.includes(" ")) {
      continue;
    }
    const label = `${scope}.${fnName}`;
    addSymbol(label, "Function", sig.replace(fnName, "").trim() || "function", sig);
  }
}

// Add common globals that are not in native modules doc sectioned with functions.
addSymbol("Promise", "Class", "global", "Promise static helpers.");
addSymbol("Promise.all", "Function", "(promises)", "Wait for all promises.");
addSymbol("Promise.race", "Function", "(promises)", "Resolve/reject with first settled promise.");
addSymbol("Promise.allSettled", "Function", "(promises)", "Wait until all promises settle.");

const symbols = [...symbolMap.values()].sort((a, b) => a.label.localeCompare(b.label));

const header = `/* eslint-disable */
// Code generated by scripts/generate-native-symbols.mjs. DO NOT EDIT.
import { CompletionItemKind } from "vscode-languageserver/node";

export type NativeSymbol = {
  label: string;
  kind: CompletionItemKind;
  detail: string;
  doc: string;
};

`;

const bodyLines = symbols.map((s) => {
  const kindRef = `CompletionItemKind.${s.kind}`;
  const label = JSON.stringify(s.label);
  const detail = JSON.stringify(s.detail);
  const doc = JSON.stringify(s.doc);
  return `  { label: ${label}, kind: ${kindRef}, detail: ${detail}, doc: ${doc} }`;
});

const output = `${header}export const nativeSymbols: NativeSymbol[] = [
${bodyLines.join(",\n")}
];
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, output, "utf8");
console.log(`generated ${path.relative(repoRoot, outFile)} with ${symbols.length} symbols`);
